package develop.cl.com.crsp.JavaBean;


public class Resume {

	private int resumeid;
	private String userid;
	private String sex;
	private int birth_year;
	private String write_time;
	private String education;
	private String picture;
	private String exp_salary;
	private String exp_area;
	private String describe;
	private int tel;

	public int getResumeid() {
		return resumeid;
	}

	public void setResumeid(int resumeid) {
		this.resumeid = resumeid;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public int getBirth_year() {
		return birth_year;
	}

	public void setBirth_year(int birth_year) {
		this.birth_year = birth_year;
	}

	public String getWrite_time() {
		return write_time;
	}

	public void setWrite_time(String write_time) {
		this.write_time = write_time;
	}

	public String getEducation() {
		return education;
	}

	public void setEducation(String education) {
		this.education = education;
	}

	public String getPicture() {
		return picture;
	}

	public void setPicture(String picture) {
		this.picture = picture;
	}

	public String getExp_salary() {
		return exp_salary;
	}

	public void setExp_salary(String exp_salary) {
		this.exp_salary = exp_salary;
	}

	public String getExp_area() {
		return exp_area;
	}

	public void setExp_area(String exp_area) {
		this.exp_area = exp_area;
	}

	public String getDescribe() {
		return describe;
	}

	public void setDescribe(String describe) {
		this.describe = describe;
	}

	public int getTel() {
		return tel;
	}

	public void setTel(int tel) {
		this.tel = tel;
	}

}
